<p><strong>BYLAW </strong><strong>VII.</strong><strong> ELECTIONS</strong></p>
<p><strong>Section 1. Transition Period and Time of Elections</strong></p>
<p>The 6th week before spring finals marks the beginning of the transition period where the outgoing executive board is responsible for ensuring the transition to the next administration. During the 6th week before finals, nominations will be open via motion at an ACM general meeting. During the 5th week before finals, a constitutional convention will be held to review the constitution and bylaws. During the 4<sup>th</sup> week before finals, elections will take place. The president, vice president, secretary, treasurer, and faculty advisor(s) shall be elected by a plurality of votes cast at the annual election meeting. The 3<sup>rd</sup> week before finals will consist of transition time where the current officers will train the cabinet elect. The 2<sup>nd</sup> week before finals the cabinet elect will run the last general meeting for the administrative year. During the last week of the semester, the incoming and outgoing executive officers will meet to transfer all files, accounts, and everything else the next administration will need. The officers for current administrative year still have the official powers and duties during this transition time and up until the end of the spring semester. It is the duty of the outgoing officers that the incoming officers have everything transferred by the end of the semester.</p>
<p>&nbsp;</p>
<p><strong>Section 2. Method of Election</strong></p>
<p>Nominations for president, vice president, secretary, treasurer, and faculty adviser will open at the general meeting 2 weeks prior to the election meeting and continuing up to the actual vote.&nbsp; Elections to fill any vacant office shall be held during any general meeting as needed.</p>
<p>All chapter balloting shall be by secret ballot. Candidates must not be present during the voting. Ballots are managed by an outgoing officer or advisor.</p>
<p>Officers shall be elected by a majority of votes cast. Members that are CSULB students that are on record as having attended 4 or more general meetings or 6 or more of a committee’s meetings may vote with the exception of the president. The president may only vote in the case of a tie.</p>
<p>The top 2 candidates may hold the position jointly if they mutually consent with the exception of president.</p>
<p>&nbsp;</p>
<p><strong>BYLAW </strong><strong>VIII.</strong><strong> METHOD TO AMEND THE BYLAWS</strong></p>
<p><strong>Section 1.</strong></p>
<p>All proposed changes to the chapter bylaws shall be approved by the executive board (president, vice president, secretary, treasurer) before being presented to the voting membership for a vote.</p>
<p><strong>Section 2.</strong></p>
<p>A vote of the majority of the two thirds of the voting membership present of the chapter shall be required to amend the chapter bylaws.</p>
<p><strong>Section 3.</strong></p>
<p>Once an amendment to the bylaws is approved by the voting membership, it must be submitted to the University for approval.</p>
<p>&nbsp;</p>
<p><strong>BYLAW IX. QUORUM</strong></p>
<p><strong>Section 1.</strong></p>
<p>A quorum shall be defined as 50% of the current officers with voting membership. Interim officers do not count towards quorum.</p>
<p><strong>Section 2.</strong></p>
<p>Any action taken without a quorum shall be brought before the members at the next general meeting where a quorum is present, and shall be automatically rescinded unless approved by a majority of the members in attendance.</p>
<p><strong>Section 3.</strong></p>
<p>General meetings must be announced at least 48 hours in advance to the voting membership.</p>
