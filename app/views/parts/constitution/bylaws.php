<p><strong>BYLAW </strong><strong>VI</strong><strong>.</strong><strong> OFFICIAL CHAPTER BUSINESS</strong></p>
<p><strong>Section 1. Projects</strong></p>
<p>Only projects with a project charter approved by a plurality of the voting membership are considered official ACM projects. Project charters are documents made to keep people who want to do projects accountable. The project charter must include:</p>
<ul>
<li>Project      Name</li>
<li>Project      Manager(s) – People who are ultimately responsible      for representing and completing the project.</li>
<li>Project Definition – A quick summary of what      the project is about.</li>
<li>Project      Scope – Definition of what the project will      consist of. Project Scope must be realistic and finite.</li>
<li>Project Goals – The output of the project      and how it will benefit the chapter and its members.</li>
<li>Project Dependencies – Dependencies (particularly      external) that the project requires to complete.</li>
<li>Project Team – People who are actively      working towards completing the project.</li>
<li>Project Stakeholders – People not directly      working on, but still involved in the project.</li>
<li>Project      Phases – General development phases the project      will undertake.</li>
<li>Project Timeline – Demonstrable milestones the      project will complete.</li>
</ul>
<ul>
<li>Project Challenges – Obstacles that have a      significant chance of setting back the project.</li>
<li>Project Finances – What the project is going to      need in terms of money to complete.</li>
</ul>
<ul>
<li>Project Estimated Completion Time – Estimated      time in hours it will take to complete the project.</li>
<li>Project Status – The      official status of the project.</li>
<li>Project Contact(s) – Contact      information for people wanting to get in touch with the people involved in      the project.</li>
</ul>
<p>&nbsp;</p>
<p>If the project charter is incomplete, it can be passed as a draft status project charter.</p>
<p>Draft status projects may set up committee times and request publicity. They may not request funds for their projects, may not use computer space, may not use server space, and the members cannot get voting membership through a draft status project. Draft status projects can be cancelled by executive decision by the president.</p>
<p>Projects with approved active charters may request funds for their project, may use computer space, may use server space, and the members get voting membership through a project charter. Projects with approved active charters get priority over projects with draft status.</p>
<p>Project charters may be amended or revoked by motion of the voting membership.</p>
<p>&nbsp;</p>
<p><strong>Section 2. Events</strong></p>
<p>Only events with an event charter approved by a plurality of the voting membership are considered an official ACM event. Event charters are documents made to keep people who want to do events accountable. The event charter must include:</p>
<ul>
<li>Event      Name</li>
<li>Event Manager(s) – People who are ultimately      responsible for representing and completing the event.</li>
<li>Event Date(s) / Time(s) – Date(s)      / Time(s) the event will take place.</li>
</ul>
<ul>
<li>Event Description – A quick summary of what the      event is about.</li>
<li>Event Purpose – A quick summary of how the      event will benefit the chapter.</li>
</ul>
<ul>
<li>Event Goals – What the event is going to      accomplish and what is the event’s output.</li>
<li>Event Requirements – What is needed to see the      event to completion.</li>
<li>Event      Team – People working on the execution of the      event.</li>
</ul>
<ul>
<li>Event Preparation Time line – Demonstrable milestones the      event committee will complete.</li>
</ul>
<ul>
<li>Event Challenges – Obstacles that have a      significant chance of setting back the event.</li>
<li>Event Finances – What the event is going to      need in terms of money to complete.</li>
<li>Expected Attendance – how many people are      expected to attend the event.</li>
<li>List of People Involved – People involved not on the      event team.</li>
</ul>
<ul>
<li>Event Status – The official status of the      event.</li>
<li>Event Contact(s) – Contact      information for people wanting to get in touch with the people involved in      the event.</li>
</ul>
<p>&nbsp;</p>
<p>If the event charter is incomplete, it can be passed as a draft status event charter.</p>
<p>Draft status events may set up committee times and request publicity. They may not request funds for their events, may not use computer space, may not use server space, and the members cannot get voting membership through a draft status event. Draft status events can be cancelled by executive decision by the president.</p>
<p>Events with approved active charters may request funds for their event, may use computer space, may use server space, and the members get voting membership through an event charter. Events with approved active charters get priority over events with draft status.</p>
<p>Event charters may be amended or revoked by motion of the voting membership.</p>
<p>&nbsp;</p>
<p><strong>Section 3. Charter Status</strong></p>
<p>A charter may have the following statuses:</p>
<ul>
<li>Draft      – The charter was approved as a preliminary charter. The charter managers      have voting membership.</li>
<li>Cancelled      – The draft charter has failed to start. The charter managers on the      charter no longer have voting membership through the charter. The charter      no longer has good standing.</li>
<li>Active      – The charter was approved. Project Charter managers gain officer status      through the charter. Event Managers and Project/Event team members are      voting members. Before a charter can be approved as active, the project      manager must have A) A limited, realistic, and finite scope B) A timeline      with demonstrable milestones and C) the ability to see the charter to      completion.</li>
<li>Hiatus      – The charter team is currently unable to currently work on the project.      The charter managers no longer have officer status through the charter.      However, the charter managers and team retain their voting membership      through the charter.</li>
<li>Suspended      – The charter team has failed to show reasonable progress on their charter      or has misused ACM resources. The charter team no longer has voting      membership. The charter managers retain their voting membership so they      may defend their charter. The charter no longer has good standing.</li>
<li>Completed/Closed      – The charter team has completed a sufficient amount of the charter task.      Charter managers and team members retain their voting membership until the      end of the administrative year.</li>
<li>Revoked      – The charter team has misused ACM resources and/or has failed to show      adequate progress. Charter managers are no longer officers through the      charter and charter team members no longer have voting membership through      the charter.</li>
</ul>
<p>&nbsp;</p>
<p><strong>Section 4. Financial</strong></p>
<p>Money from any ACM account (including any cash box), may only be used on official ACM &nbsp;projects, events, or official business approved by a plurality of the voting membership. Any unofficial use of ACM financial resources is considered to be theft.</p>
<p>&nbsp;</p>
<p><strong>Section 5. Accounts</strong></p>
<p>The faculty adviser must have administrative information to any ACM account.</p>
<p>&nbsp;</p>
<p><strong>Section 6. Priority Use of ACM Resources</strong></p>
<p>Work takes priority over recreation for use of ACM resources, which includes (but is not limited to) the ACM office, ACM financial resources, and time used in ACM meetings.</p>
<p>&nbsp;</p>
<p><strong>Section 7. Executive Discretionary Fund</strong></p>
<p>If the executive board needs to pay an expense before the next general meeting, the executive board may use up to $50 from ACM’s funds. The expense must be documented and have the written approval of all of the executive officers (the president, vice president, secretary, and treasurer). If any one of the executive officers does not sign off on the purchase, it is considered theft.</p>
<p>&nbsp;</p>
<p>The documented purchase must then be approved via motion at the next general meeting. If the expense is not approved, the discretionary fund is suspended until the expense is reimbursed or approved.</p>
<p>&nbsp;</p>
<p>During the last 6 weeks of the spring semester, the executive discretionary fund may not be used due to the focus of that time is transferring responsibility to a new executive board.</p>
<p>&nbsp;</p>
<p><strong>Section 8. Market Discretionary Fund</strong></p>
<p>If the market needs to restock before the next general meeting, the treasurer may grant the market manager up to $100 from ACM’s funds. The purchases must be documented and have the written approval of the treasurer. If the treasurer does not sign off on the purchases, it is considered theft.</p>
<p>&nbsp;</p>
<p>The documented purchases must then be approved via motion at the next general meeting. If the expense is not approved, the discretionary fund is suspended until the expense reimbursed or approved.</p>
<p>&nbsp;</p>
<p>The market discretionary fund may only be used to purchase items to be sold in the ACM market.</p>
<p>&nbsp;</p>
<p><strong>Section 9. Office Keys</strong></p>
<p>Only officers may hold keys to the ACM office. Holding a key is a privilege, not a right. Any officer who has a key must announce their office hours at a general meeting and maintain those office hours. These office hours can be updated at a general meeting. Official office hours must be posted for the membership. Failure to maintain reasonable office hours can be corrected by having their key privileges revoked via motion by the voting membership.</p>
