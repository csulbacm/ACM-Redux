<p><strong>BYLAW X PARLIAMENTARY AUTHORITY</strong></p>
<p>The current edition of Robert’s Rules of Order shall serve as the official parliamentary authority for the conduct of all chapter meetings.</p>
<p><strong>Section 1. Limits of Chief Parliamentarian Power</strong></p>
<p>The chief parliamentarian presiding over general meetings may not make motions or vote. The exception to not voting is when there is a tie, and then the chief parliamentarian makes the tie-breaking vote.</p>
<p><strong>Section 2. Defining Official Business</strong></p>
<p>Only business recorded in approved minutes is considered official.</p>                