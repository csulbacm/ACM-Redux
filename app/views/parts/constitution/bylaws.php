<h3><strong>Bylaws of&nbsp;The Association for Computing Machinery&nbsp;California State University, Long Beach&nbsp;Student Chapter</strong></h3>
<p><em>(Last Revised&nbsp;Feb 8, 2008)</em></p>
<p><strong> </strong></p>
<p><strong>BYLAW I. MEMBERSHIP</strong></p>
<p><strong>Section 1. Voting Members</strong></p>
<p>All currently enrolled CSULB Students meeting the following requirements shall have the full rights and responsibilities of voting membership:</p>
<ul>
<li>Be a current elected officer, a current appointed officer, a current committee chair, a team member on record on a project charter active and still in good standing during the current administrative year, a team member on record on an ACM event charter active and still in good standing during the current administrative year</li>
</ul>
<p><strong>or</strong></p>
<p><strong> </strong></p>
<ul>
<li>Have current ACM national membership (all officers must be national members), and be on record on the membership chair’s list of national members.</li>
</ul>
<p><strong>Section 2. General Members</strong></p>
<p>Anyone interested in the goals and activities of the chapter may attend any meeting as a general member.</p>
<p><strong>Section 3. Expulsion of Members</strong></p>
<p>Any member may be suspended or expelled from the organization for conduct contrary to the constitution of the organization or for conduct that grossly impairs the rights of the members to enjoy the benefits of the organization. Such activities include but are not limited to disregarding parliamentary rule, tampering with the security system or being disrespectful to other members. The offense must be submitted in writing by a member to the voting membership. &nbsp;After the offense has been submitted to the voting membership the offending member shall have the right to a hearing before the organization at a general meeting and may be suspended or expelled only upon the affirmative vote of three-fourths of the voting members present. &nbsp;The offending member has the right to appeal an adverse decision at a general meeting, and such offending members shall be reinstated only if the suspension or expulsion is overturned by a three-fourths vote of voting members present.</p>
<p><strong>Section 4. ASI</strong><strong> &amp; UNIVERSITY</strong><strong> Regulations</strong></p>
<p>The chapter and its members shall abide by all regulations from ASI and The University.</p>
<p><strong> </strong></p>
<p><strong>Section 5. </strong></p>
<p>Up to<strong> </strong>a maximum of 20 percent of the members of a student organization may be individuals who are not CSULB students, e.g., community members, students at other colleges. Only students enrolled at CSULB may vote on issues that come before the student organization.</p>
<p>&nbsp;</p>
<p><strong> </strong></p>
<p><strong>BYLAW II. DUES</strong></p>
<p>There are no dues to be a CSULB ACM chapter member.</p>
<p>&nbsp;</p>
<p><strong>BYLAW III. OFFICERS</strong></p>
<p><strong>Section 1. Powers And Duties of Elected Officers</strong></p>
<p>In order to assure the smooth transition between officers, all officers shall have the duty of training their replacements and/or providing adequate written records of their activities to allow new officers to quickly prepare themselves to undertake their duties. The voting membership may delegate or rearrange all chapter responsibilities, as it deems appropriate. In the absence of specific delegation, the following description of duties shall apply.</p>
<p>&nbsp;</p>
<p><strong>A. President</strong></p>
<p>The president shall be the principal chapter officer and is responsible for supporting the membership in its activities. The president’s duties include:</p>
<ul>
<li>Presiding      as chief parliamentarian at general meetings of the chapter.</li>
<li>Ensuring      that all officers and members act in compliance with the Constitution and      Bylaws of the Chapter in all chapter activities by providing feedback on      how their work and conduct is important to the interests of the chapter.</li>
<li>Implementing      policies that apply members’ CECS skills, broaden their soft skills, make      them more employable, and extend their network of connections.</li>
<li>Implementing      polices that bring prestige to the chapter, CECS department, CSULB, and      ACM national.</li>
<li>Promoting      outreach and competitions.</li>
<li>Promoting      a culture of CECS students that go above and beyond.</li>
<li>Promoting      a culture of inclusivity.</li>
<li>Respecting      the autonomy of the voting membership while also holding them accountable.</li>
<li>Ensuring      the success of ACM projects by making sure that projects are in compliance      with their project charters and giving feedback on their progress.</li>
<li>Delegating      the responsibilities of empty officer positions.</li>
<li>Keeping      the chapter workload manageable.</li>
<li>Making      sure every member that wants to be involved has something to do.</li>
<li>Using      the appointed and project manager officer positions to encourage new      member participation.</li>
<li>Being      ultimately responsible for security issues in the ACM office.</li>
<li>Making      sure ACM resources are secure.</li>
<li>Making      sure that the Faculty Advisor has administrative access to all ACM      accounts.</li>
<li>Ensuring      that ACM resources such as server space and office space are used and      organized effectively and efficiently.</li>
<li>Making      sure that the organization can transition smoothly to the next      administration.</li>
</ul>
<p>&nbsp;</p>
<p>The president will be responsible for the duties of executive officer positions (vice president, secretary, treasurer) if the position is vacant. The president may delegate the duties of empty executive officer positions.</p>
<p>&nbsp;</p>
<p><strong>B. Vice President</strong></p>
<p>The vice president is the chief of staff for the chapter. The vice president shall preside at meetings in the absence of the president, and assist the president in supporting the chapter. The vice president’s duties shall also include:</p>
<ul>
<li>Ensuring that all officers      are performing their duties.</li>
<li>Making sure that that      all committees are being productive.</li>
<li>Keeping      track of who needs to do what by when.</li>
</ul>
<p>The vice president will be responsible for the duties of the following officer positions if the position is vacant:</p>
<ul>
<li>Event      Coordinator</li>
<li>Office      Technician</li>
<li>AESB      Representative</li>
<li>Librarian</li>
</ul>
<p>The vice president may delegate the duties of the Event Coordinator, Office Technician, AESB Representative, and Librarian.</p>
<p>&nbsp;</p>
<p><strong>C. Secretary</strong></p>
<p>The secretary is the chief record keeper for the chapter. The secretary shall keep the minutes of all general meetings where business is done. Other duties of the secretary shall include:</p>
<ul>
<li>Preparing and submitting the chapter’s official annual activity report and other reports to ACM National, as well as all reports required of the chapter by the university or the ASI.</li>
<li>Submitting documents such as the ORC and ARC to SLD.</li>
<li>Keeping a copy of&nbsp;the records/revisions of all active projects and all active committees.</li>
<li>Keeping a copy of the constitution/bylaws and all their revisions.</li>
<li>Taking minutes during the general meetings.</li>
<li>Making sure that summaries of reports/announcements given, motions made, verbal agreements, claims/promises relevant to the chapter, and any other information during general meetings needed to hold people accountable to the chapter are reflected in the minutes.</li>
<li>Making sure that records of motions include who made the motion, who seconded the motion, and how the motion passed or failed.</li>
<li>Having all records and revisions of minutes and charters on hand for general meetings.</li>
<li>Making sure that all minutes, agendas, charters, and their revisions are available to the membership.</li>
<li>Making sure that the constitution and bylaws and their revisions are available to the membership.</li>
<li>Making sure all records and communications are done in a professional manner.</li>
<li>Finding methods of keeping people accountable that is flexible and efficient.</li>
</ul>
<p>The secretary will be responsible for the duties of the following officer positions if the position is vacant:</p>
<ul>
<li>Contact      Liaison</li>
<li>Membership      Chair</li>
<li>Webmaster</li>
<li>Publicity      and Recruitment Chair</li>
</ul>
<p>The secretary may delegate the duties of the Contact Liaison, Membership Chair, Webmaster, and Publicity and Recruitment Chair.</p>
<p>&nbsp;</p>
<p><strong>D. Treasurer</strong></p>
<p>The treasurer is the chief financial officer for the chapter. The treasurer shall monitor all accounts, pay all bills, and maintain the chapter’s financial records, in both “digital” and “hard copy” format ; including securing copies of all records in case of theft or damage. The treasurer’s duties shall also include:</p>
<ul>
<li>Maintaining the financial records of the ACM chapter.</li>
<li>Making disbursements from chapter accounts following the authorization of the voting membership, whose approval shall be recorded in the minutes of its meetings.</li>
<li>Submitting the annual financial report to ACM National.</li>
<li>Presenting an expense report whenever ACM money is spent.</li>
<li>Finding sources of revenue for the chapter.</li>
</ul>
<p>The treasurer will be responsible for the duties of the following officer positions if the position is vacant:</p>
<ul>
<li>Fundraising      Chair</li>
<li>Market      Manager</li>
</ul>
<p>The treasurer may delegate the duties of the Fundraising Chair and Market Manager.</p>
<p>&nbsp;</p>
<p><strong>E. Webmaster</strong></p>
<p>The webmaster is in charge of maintaining the content of the ACM website and social media pages of ACM. The webmaster’s duties also include:</p>
<ul>
<li>Posting      the newsletter from the membership chair on the ACM website.</li>
<li>Keeping      a directory of the chapter’s documents for each general meeting including      agendas, original interim minutes, minutes that were approved, charters      that were passed, charters that where amended, changes to the      constitution/bylaws that were made during the meeting.</li>
<li>Keeping      a directory for current charters.</li>
<li>Keeping      a directory for the constitution and bylaws and their revisions.</li>
<li>Keeping      a guide to parliamentary procedure on the website.</li>
<li>Managing      and updating the ACM Facebook and YouTube pages.</li>
<li>Providing      interesting and useful information for the membership on the ACM website      and social media pages.</li>
<li>Providing      web resources for the membership.</li>
</ul>
<p>&nbsp;</p>
<p><strong>F. AESB Representative</strong></p>
<p>The AESB representative is chief diplomat to other student organizations. The AESB representative must keep minimum attendance at the AESB meetings. The AESB representative duties include:</p>
<ul>
<li>Keeping the general body      informed of the proceedings of AESB, and its subsidiaries at ACM Meetings.</li>
<li>Maintaining good      relationships with other organizations within AESB and ASI.</li>
<li>Finding      temporary replacements when he/she cannot make the AESB meetings.</li>
<li>Building      connections with other student organizations.</li>
</ul>
<p>&nbsp;</p>
<p><strong>G. Project Manager</strong></p>
<p>A project manager is the chief ACM executive for an ACM sponsored project. In order to become a project manager, a member must propose a project charter to the voting membership. If the project charter is approved, the member(s) who are listed as project manager(s) on the charter become project managers and full officers of ACM.</p>
<p>A project manager’s duties include:</p>
<ul>
<li>Holding regular project      committee meetings for the project he/she is assigned.</li>
<li>Ensuring      that communication with the project team is consistent and regular.</li>
<li>Keeping      communication with membership professional.</li>
<li>Keeping      the project scope and workload manageable.</li>
<li>Presenting      project SCRUM reports to the voting membership which include A) what the      project team has done since the last general meeting, B) what they’re      going to be doing between now and the next general meeting and C) what      difficulties they are currently facing.</li>
<li>Keeping track of attendance at the committee meetings      and submitting it to the membership chair so that their team members have election voting      privileges.</li>
<li>Submit      project charter changes when changes need to be done to the project      timeline, project scope, project team, etc.</li>
<li>Attend      general meetings to represent the project committee.</li>
<li>Above all, get the project done by the time dictated in      the project charter within the      scope of the project charter.</li>
</ul>
<p>The ultimate responsibility of the project falls on the project manager(s).</p>
<p>&nbsp;</p>
<p><strong>H. Fundraising Chair</strong></p>
<p>The fundraising chair is responsible for finding sources of revenue for the chapter. The fundraising chair’s duties include:</p>
<ul>
<li>Organizing fundraising      committee meetings and fundraising events for the chapter.</li>
<li>Preparing      event charters for fundraising events.</li>
<li>Overseeing chartered      fundraising events.</li>
<li>Keeping track of attendance      at the committee meetings and submit it to the membership chair so that his/her team members have election voting      privileges.</li>
<li>Making sure the      treasurer has financial records from any fundraisers.</li>
</ul>
<p>&nbsp;</p>
<p><strong>I. Event Coordinator</strong></p>
<p>The event coordinator is responsible for organizing social events to increase participation in the chapter.&nbsp; The event coordinator’s duties include:</p>
<ul>
<li>Holding      regular event committee meetings to organize regular events for ACM.</li>
<li>Preparing      event charters for social events.</li>
<li>Overseeing      and assisting in chartered events.</li>
<li>Keeping      track of attendance at the committee meetings and submitting it to the      membership chair so that his/her team members have election voting      privileges.</li>
<li>Hosting      Alumni events to promote connections with industry.</li>
</ul>
<p>&nbsp;</p>
<p><strong>J. Membership Chair</strong></p>
<p>The membership chair is the chief membership communicator and membership record keeper for the chapter. The membership chair’s duties include:</p>
<ul>
<li>Managing attendance records      from general and committee meetings.</li>
<li>Writing,      managing, and mailing the ACM newsletter.</li>
<li>Making sure the secretary has      access to membership records.</li>
<li>Keeping track of each member’s      executive positions, appointed positions, project manager positions, event      manager positions, project team participation, event team participation,      committee chair positions, and national membership.</li>
<li>Keeping all communications sent out      to the membership short, to the point, and professional.</li>
<li>Minimizing the volume of      communication sent out.</li>
<li>Keeping track of who is eligible to      vote in elections.</li>
<li>Having      membership records ready for general body meetings.</li>
<li>Keeping      track of the alumni list for future alumni events.</li>
</ul>
<p>&nbsp;</p>
<p><strong>K. Contact Liaison</strong></p>
<p>The contact liaison is external communications director for the chapter. The contact liaison’s duties include:</p>
<ul>
<li>Reaching out to companies in      the CECS industry and finding contacts for      projects and guest speakers.</li>
<li>Communicating      with the CECS department.</li>
<li>Getting information through      other organizations and meeting with companies at job fairs to find      contacts.</li>
<li>Requesting a parking permit      for guest speakers and providing certificates to the guest speakers.</li>
<li>Keeping abreast of the      proceedings of ACM national, and maintaining relations with ACM national.</li>
<li>Keeping      a record of contacts and making sure the secretary has a copy.</li>
</ul>
<p>&nbsp;</p>
<p><strong>L. </strong><strong>Market Manager</strong><strong> </strong></p>
<p>The Market Manager maintains items sold by ACM. The Market Manager may only sell items approved by the voting membership and the university. Once items are approved by the voting membership, the market manager must also get the items approved by the university. Market manager duties include:</p>
<ul>
<li>Keeping      a record of which items are approved for sale.</li>
<li>Keeping      a record of purchases made to sell in the market.</li>
<li>Restocking      the market.</li>
<li>Keeping      a track of inventory.</li>
<li>Finding      items that will maximize revenue of the market.</li>
<li>Finding      items that the membership wishes to purchase.</li>
</ul>
<p>&nbsp;</p>
<p><strong>M. Publicity &amp; Recruitment Chair</strong></p>
<p>The publicity chair’s job is to promote ACM meetings, events, and the organization itself. Duties include:</p>
<ul>
<li>Creating and posting fliers      for meetings and events.</li>
<li>Keeping the ACM bulletin      boards updated and organizing recruitment events [Week of Welcome,      Engineering Organization Fair, SOAR, etc].</li>
<li>Submitting the list of      recruits to the membership chair.</li>
<li>If      no historian is appointed, the publicity chair takes over the duties of      the historian.</li>
<li>Finding methods to promote ACM’s      image.</li>
<li>Finding methods to increase      membership.</li>
</ul>
<p>&nbsp;</p>
<p><strong>N. Office Technician</strong></p>
<p>The office technician shall make sure that hardware and software used in the ACM office is maintained. Duties include:</p>
<ul>
<li>Ensuring      that the hardware and software are usable for the membership.</li>
<li>Handling      software/hardware requests from the membership.</li>
<li>Making      sure the workstations look professional.</li>
<li>Finding      useful hardware and software for use of the membership.</li>
</ul>
<p>&nbsp;</p>
<p><strong>O. Librarian</strong></p>
<p>The librarian must maintain the books in the ACM library. Duties include:</p>
<ul>
<li>Keeping      inventory of books currently in the library.</li>
<li>Seeking      sources of book donations.</li>
<li>Organizing      fundraisers to buy books for the library.</li>
<li>Finding      books and resources that will benefit the membership.</li>
</ul>
<p>&nbsp;</p>
<p><strong>P. Historian</strong></p>
<p>The historian shall manage photos, video, and other archival media of ACM meetings and events. Duties include:</p>
<ul>
<li>Taking      photos of ACM activities.</li>
<li>Taking      and editing video of ACM activities.</li>
<li>Making      sure that the secretary, webmaster, and publicity chair have access to      photos and videos.</li>
<li>Creating      media to document the activities of the chapter.</li>
</ul>
<p>&nbsp;</p>
<p><strong>Q. Faculty Advisor</strong></p>
<p>A faculty advisor is a faculty member that assists the chapter. Duties include:</p>
<ul>
<li>Serving      as a liaison between ACM and the CECS department.</li>
<li>Assisting      the organization by providing contacts for projects, events, competitions,      and guest speakers.</li>
<li>Providing      resources to help with projects/events.</li>
<li>Making      sure the organization is following the constitution and bylaws.</li>
<li>Being      hands off. The students should be doing the work.</li>
</ul>
<p>&nbsp;</p>
<p><strong>R. Alumni Advisor</strong></p>
<p>An alumni advisor is a CSULB graduate that assists the chapter after they are no longer attending CSULB. Duties include:</p>
<ul>
<li>Assisting      the organization by providing contacts for projects, events, competitions,      and guest speakers.</li>
<li>Providing      resources to help with projects/events.</li>
<li>Assisting      in projects and events.</li>
<li>Advising      the chapter on operations based on their experience as chapter members.</li>
</ul>
<p><strong>Section 2. Filling Vacancies</strong></p>
<p>Vacant elected chapter officer positions shall only be filled by a majority vote from the voting membership in attendance in a valid election as defined in the chapter constitution and bylaws if the position is an elected position. If the position is appointed, then the president can appoint a replacement temporarily until the replacement can be approved by the voting membership by majority vote at the next general meeting.</p>
<p>&nbsp;</p>
<p><strong>A. Limits to Appointments (Edgar Loophole)</strong><strong> </strong></p>
<p>If an appointee is not approved at the next general meeting after their appointment, they can no longer serve as an interim appointed officer for that position for the duration of the administrative year. However, they can still be brought before the voting membership for approval.</p>
<p>&nbsp;</p>
<p><strong>B. President Elect Appointments</strong></p>
<p>When the president for the next administration is officially elected, he/she can make interim appointments for their incoming administration. However, they cannot be approved until the president’s term officially begins.</p>
<p>&nbsp;</p>
<p><strong>Section 3. Recall of Officers</strong></p>
<p>The voting membership may, with a two thirds vote of its voting members, present a recommendation to the members to remove a chapter officer for incapacitation, inactivity, or other valid reason at any regularly scheduled general meeting. The members may remove the officer only upon a vote of two thirds of the voting members in attendance at any regularly scheduled general meeting following by at least 7 days where the meeting &nbsp;the recommendation for removal was presented.</p>
<p>&nbsp;</p>
<p><strong>Section 4. Eligibility of Officers</strong></p>
<p>To be eligible for office in the organization, a candidate for office must be carrying a minimum of six (6) undergraduate units or three (3) graduate units of regular university credit per semester at CSULB. All student representatives must be matriculated at the university, maintaining&nbsp;a minimum overall, on-campus cumulative 2.5 grade point average (GPA) at all times, and a 2.5 GPA in every semester while in office, and in the semester prior to running for office, and not be on probation of any kind. All officers must be ACM national members and must have the appropriate ASI certification(s).</p>
<p>&nbsp;</p>
<p><strong>Section 5. Terms of Office</strong></p>
<p>The term of office for any officer shall be one year starting at the beginning of the summer session and ending at the end of the next spring session. Officers may stand for reelection any number of terms. However, since a primary purpose of the chapter is to promote leadership opportunities for all members, any officer who has served for more than two years shall have, as his or her primary responsibility, to recruit and train replacement officer candidates.</p>
<p>&nbsp;</p>
