<p><strong>BYLAW IV.  ADVISORS: </strong></p>
<p><strong>Section 1.</strong></p>
<p>There shall be at least one advisor from the faculty or professional staff elected by the organization.</p>
<p><strong>Section 2.</strong></p>
<p>The faculty advisors shall serve a term of one year and will be elected at the same time as the officers of the organization.</p>
<p><strong>Section 3.</strong></p>
<p>The advisor(s) shall be ex-officio, non-voting member(s).</p>
<p><strong>Section 4.</strong></p>
<p>The president can appoint Alumni advisors for ACM members that have graduated college and no longer attend CSULB.</p>
