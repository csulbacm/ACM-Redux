<p><strong>BYLAW V</strong><strong>.</strong><strong> COMMITTEES</strong></p>
<p>General meetings are for the chapter to discuss and decide what the chapter is going to do. Committee meetings are for discussing details of how the chapter will execute its activities. If a committee is failing to complete their task, the vice president or president can disband the committee by executive order at a general meeting. The voting membership can also disband committee via motion.</p>
<p><strong>Section 1. Project Committees</strong></p>
<p>Each project committee shall first come up with a project charter to outline their proposed project. Then one of the project members proposes his/her project charter to voting membership for approval. If the project charter is approved, the project committee shall hold regular meetings to finish the project. The project committee must also present regular project status updates at the general meetings. A charter that is incomplete can be approved as a draft to set up in interim project committee.</p>
<p><strong>Section 2. Event Committee</strong></p>
<p>The event committee lead by the event coordinator meets to create event charters for ACM events. The event charters must be proposed to voting membership for approval. If the event charter is approved, the event committee shall then focus their effort hosting the event. A charter that is incomplete can be approved as a draft to set up in interim event committee.</p>
<p><strong>Section 3. Fundraising Committee</strong></p>
<p>The fundraising committee lead by the fundraising chair meets to create event charters for ACM fundraising events. The event charters must be proposed to voting membership for approval. If the event charter is approved, the event committee shall then focus their effort hosting the event. A charter that is incomplete can be approved as a draft to set up in interim event committee.</p>
<p><strong>Section 4: Constitution/Bylaws Committee</strong></p>
<p>The Constitution/Bylaws Committee meets to review, explain, and write proposed changes to the constitution/bylaws. The head of the bylaws committee must be appointed by motion of the voting membership.</p>
<p><strong>Section 5: Special Interest Group Committees</strong></p>
<p>Special Interest Groups can be formed via motion of the voting membership. Special Interest Groups meet regularly to discuss and participate in activities related to their special interest. The goal of Special Interest Groups is to start up ACM Projects, ACM Events, and to increase member participation.</p>
<p><strong> </strong></p>
<p><strong> </strong></p>
<p><strong>Section 6: Special Committees</strong></p>
<p>If a task or decision requires a group of members to execute, a special committee may be formed to complete the task via motion.</p>
